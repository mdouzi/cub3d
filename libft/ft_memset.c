#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*p;

	i = 0;
	p = (char *)b;
	while (i < len)
	{
		*p = c;
		p++;
		i++;
	}
	p = p - i;
	return (p);
}
